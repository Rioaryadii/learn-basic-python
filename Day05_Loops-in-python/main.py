#Day 05 - Loops in Python

def main():
    # For loop example
    for i in range(5):
        print(f"Iteration {i}")

    # While loop example
    count = 0
    while count < 5:
        print(f"Count is {count}")
        count += 1

if __name__ == "__main__":
    main()